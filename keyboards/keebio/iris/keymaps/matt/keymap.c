static bool entquot_active = false;
static bool caps_enabled = false;
#define LSHIFT_ON (keyboard_report->mods & MOD_BIT(KC_LSFT))

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case ENTCAPS:
        {
          uint8_t kc = KC_ENT; // Default keycode

          // Key press with shift held
          if (LSHIFT_ON && entquot_active == false) {
              kc = KC_CAPS;
              entquot_active = true;
          } else if (entquot_active == true) { // Key release
              kc = KC_CAPS;
              entquot_active = false;
              caps_enabled = !caps_enabled;
          }

          if (record->event.pressed) {
              register_code(kc);
          } else {
              unregister_code(kc);
          }

        }
      return false;
      break;
  }
  return true;
}

void encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        if (clockwise) {
            tap_code(KC_VOLU);
        } else {
            tap_code(KC_VOLD);
        }
    }
    else if (index == 1) {
        if (clockwise) {
            tap_code(KC_PGDN);
        } else {
            tap_code(KC_PGUP);
        }
    }
}
